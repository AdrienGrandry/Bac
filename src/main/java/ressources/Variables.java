package ressources;

public class Variables {
    private int height;
    private int width;


    public void setHeight(int height) {
        this.height = height;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    
    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }
}